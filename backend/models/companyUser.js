const mongoose = require('mongoose');

const companyUserSchema = mongoose.Schema({
    company_name: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
});

module.exports = mongoose.model('company_user', companyUserSchema);